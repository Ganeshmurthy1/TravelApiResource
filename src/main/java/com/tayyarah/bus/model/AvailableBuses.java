import java.io.Serializable;
import java.util.List;

public class AvailableBuses implements Serializable{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String origin;
	private String destination;
	private String onwardDate;
	private String returnDate;
	private String currency;
	private String searchKey;
	private String transactionkey;
	private Status status;
	private List<AvailableBus> availableBus;
	private BusSearchFilters busSearchFilters;
	
	
	public String getOrigin() {
		return origin;
	}
	public String getDestination() {
		return destination;
	}
	public String getOnwardDate() {
		return onwardDate;
	}
	public String getReturnDate() {
		return returnDate;
	}
	public String getCurrency() {
		return currency;
	}
	public Status getStatus() {
		return status;
	}
	public List<AvailableBus> getAvailableBus() {
		return availableBus;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public void setOnwardDate(String onwardDate) {
		this.onwardDate = onwardDate;
	}
	public void setReturnDate(String returnDate) {
		this.returnDate = returnDate;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public void setStatus(Status status) {
		this.status = status;
	}
	public void setAvailableBus(List<AvailableBus> availableBus) {
		this.availableBus = availableBus;
	}
	public BusSearchFilters getBusSearchFilters() {
		return busSearchFilters;
	}
	public void setBusSearchFilters(BusSearchFilters busSearchFilters) {
		this.busSearchFilters = busSearchFilters;
	}
	public String getSearchKey() {
		return searchKey;
	}
	public String getTransactionkey() {
		return transactionkey;
	}
	public void setSearchKey(String searchKey) {
		this.searchKey = searchKey;
	}
	public void setTransactionkey(String transactionkey) {
		this.transactionkey = transactionkey;
	}
	
}
