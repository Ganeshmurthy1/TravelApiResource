package com.tayyarah.common.util.enums;

public enum PaymentEnum {
	INITIATED (0, "Initiated"),   
	PAYLATER (1, "PayLater"),  
	PARTIALLYPAID (2, "PartiallyPaid"),  
	SUCCESS (3, "Successfull"),
    ERROR   (4, "Failed"),
	REFUNDED   (5, "Refunded");  
	
	private final int code;   
    private final String message; 
    PaymentEnum(int code, String message) {
        this.code = code;
        this.message = message;
    }
    private int code() { return code; }
    private String message() { return message; }   
}
