package com.tayyarah.hotel.model;

import java.io.Serializable;

public class CancelPenalty implements Serializable {
		/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

		private String CancelDescription;

	    private AmountPercent AmountPercent;

	    private Deadline Deadline;

	    public String getCancelDescription ()
	    {
	        return CancelDescription;
	    }

	    public void setCancelDescription (String CancelDescription)
	    {
	        this.CancelDescription = CancelDescription;
	    }

	    public AmountPercent getAmountPercent ()
	    {
	        return AmountPercent;
	    }

	    public void setAmountPercent (AmountPercent AmountPercent)
	    {
	        this.AmountPercent = AmountPercent;
	    }

	    public Deadline getDeadline ()
	    {
	        return Deadline;
	    }

	    public void setDeadline (Deadline Deadline)
	    {
	        this.Deadline = Deadline;
	    }

	    @Override
	    public String toString()
	    {
	        return "CancelPenalty [CancelDescription = "+CancelDescription+", AmountPercent = "+AmountPercent+", Deadline = "+Deadline+"]";
	    }
	}
				
			